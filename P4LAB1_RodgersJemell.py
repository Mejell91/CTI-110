#Square
import turtle          
win = turtle.Screen()  
timmy = turtle.Turtle()

# add some display options
timmy.pensize(4)            # increase pensize (takes integer)
timmy.pencolor("orange")     # set pencolor (takes string)


for item in range(4):
    #Actions to happen
    timmy.forward(100)
    timmy.left(90)

